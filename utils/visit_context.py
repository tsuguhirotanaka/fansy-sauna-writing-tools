def get_visit_context(count: int) -> str:
    if count == 1:
        return "初訪問（1回目）。初めて訪れた新鮮な驚きや発見を中心に書く。専門用語は控えめにし、ドキドキ感や期待を込める。"
    elif count <= 3:
        return f"訪問{count}回目。施設の特徴を把握し始めた段階。気に入ったポイントを掘り下げ、また来たいという気持ちを込めて書く。"
    elif count <= 9:
        return f"訪問{count}回目のリピーター。施設への深い愛着と理解がある。他施設との違いや、自分だけのお気に入りポイントを語れる視点で書く。"
    else:
        return f"訪問{count}回以上の常連。施設を熟知した視点から語る。季節ごとの違いや細かいこだわり、スタッフとの関係性なども感じさせる深みのある内容で書く。"
